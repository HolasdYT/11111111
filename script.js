    let currentPurpose = 'all';
    let currentForm = 'all';
    let searchQuery = '';

    function applyFilters() {
        $('.catalog-card').each(function() {
            const cardPurpose = $(this).data('purpose');
            const cardForm = $(this).data('form');
            const cardName = $(this).find('h3').text().toLowerCase();

            const matchPurpose = (currentPurpose === 'all' || cardPurpose === currentPurpose);
            const matchForm = (currentForm === 'all' || cardForm === currentForm);
            const matchSearch = cardName.includes(searchQuery);

            if (matchPurpose && matchForm && matchSearch) {
                $(this).fadeIn(200).css('display', 'flex');
            } else {
                $(this).hide();
            }
        });
    }

    $('#filter-purpose .f-btn').on('click', function() {
        $('#filter-purpose .f-btn').removeClass('active');
        $(this).addClass('active');
        currentPurpose = $(this).data('val');
        applyFilters();
    });

    $('#filter-form .f-btn').on('click', function() {
        $('#filter-form .f-btn').removeClass('active');
        $(this).addClass('active');
        currentForm = $(this).data('val');
        applyFilters();
    });

    $('#search-input').on('input', function() {
        searchQuery = $(this).val().toLowerCase();
        applyFilters();
    });

